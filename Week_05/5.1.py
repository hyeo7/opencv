import tensorflow as tf
from tensorflow.keras import layers, models

# =============================================================================
# 1. MNIST 데이터셋 로드 및 분할
# tensorflow.keras.datasets에서 MNIST 데이터셋을 불러옴
#           데이터를 훈련 세트와 테스트 세트로 분할
# =============================================================================
mnist = tf.keras.datasets.mnist
(x_train, y_train), (x_test, y_test) = mnist.load_data()

# =============================================================================
# 2. 데이터 전처리 (정규화)
# 픽셀 값을 0~1 사이로 변환하여 신경망 가중치 학습의 안정성 확보
# =============================================================================
x_train, x_test = x_train / 255.0, x_test / 255.0

# =============================================================================
# 3. 간단한 신경망 모델 구축
# Sequential 모델과 Dense 레이어를 활용하여 신경망 구성
# =============================================================================
model = models.Sequential([
    # 입력층: 28x28 크기의 2D 흑백 이미지를 784개의 1D 벡터로 평탄화
    layers.Flatten(input_shape=(28, 28)),
    
    # 은닉층: 128개의 노드와 비선형 활성화 함수 ReLU 사용
    layers.Dense(128, activation='relu'),
    
    # 정규화층: 과대적합 방지를 위해 학습 시 20%의 노드를 무작위로 끔
    layers.Dropout(0.2),
    
    # 출력층: 10개의 숫자(0~9) 클래스 확률을 계산하기 위한 Softmax 활성화 함수
    layers.Dense(10, activation='softmax')
])

# =============================================================================
# 4. 모델 컴파일 및 훈련
# =============================================================================
# 다중 클래스 분류이므로 sparse_categorical_crossentropy 손실 함수 사용
model.compile(optimizer='adam',
              loss='sparse_categorical_crossentropy',
              metrics=['accuracy'])

print("\n--- 신경망 학습 시작 ---")
model.fit(x_train, y_train, epochs=5)

# =============================================================================
# 5. 정확도 평가
# =============================================================================
print("\n--- 테스트 세트 정확도 평가 ---")
test_loss, test_acc = model.evaluate(x_test,  y_test, verbose=2)
print(f"\n✅ 모델의 최종 테스트 정확도: {test_acc * 100:.2f}%")